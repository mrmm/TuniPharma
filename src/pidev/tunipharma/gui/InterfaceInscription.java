    private void btAjoutCptConfirmerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAjoutCptConfirmerActionPerformed
        // TODO add your handling code here:
        if (GUIUtil.checkForm(panelAjoutCpt)) {
            //Ajout du compte dans la base de donnée
            if (ComptesDAO.getInstance().readByEmail(txtAjoutCptEmail.getText()) == null) {
                int typeCpt = comboBoxAjoutCptTypeCpt.getSelectedIndex() + 2;
                String pass = String.valueOf(txtAjoutCptMDP.getPassword(), 0, txtAjoutCptMDP.getPassword().length);
                Compte cpt = new Compte(-1, txtAjoutCptNom.getText(),
                    txtAjoutCptPrenom.getText(),
                    txtAjoutCptAddresse.getText(),
                    txtAjoutCptEmail.getText(),
                    pass,
                    Integer.parseInt(txtAjoutCptTel.getText()),
                    typeCpt,
                    (typeCpt == Compte.COMPTE_CLIENT ? true : false)
                );
                ComptesDAO.getInstance().create(cpt);
                if (typeCpt == Compte.COMPTE_PHARMACIEN) {
                    DemandesDAO.getInstance().create(new Demande(-1, Demande.DEMANDE_COMPTE, null, cpt.getId_cpt(), cpt.getId_cpt()));
                    GUIUtil.showMsgBox(this,"Demande de compte envoyé !");
                }
                //creation de boit message
                BoitesMessagesDAO.getInstance().create(new BoiteMessages(-1, cpt.getId_cpt()));
                //GUIUtil.showMsgBox(cpt.toString());
                GUIUtil.resetForm(panelAjoutCpt);
            } else {
                GUIUtil.showMsgBox(this, "Cet email existe deja !!");
            }
        }
    }//GEN-LAST:event_btAjoutCptConfirmerActionPerformed

